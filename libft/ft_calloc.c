#include "libft.h"

// Allocates and zero-initializes memory.
// Allocates space for 'count' objects of size 'size'.
// Returns pointer to allocated memory or NULL if allocation fails.
void	*ft_calloc(size_t size)
{
	void	*res;

	res = malloc(size);
	if (res)
		ft_bzero(res, size);
	return (res);
}
