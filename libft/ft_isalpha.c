#include "libft.h"

// Checks if character is alphabetic (A-Z or a-z).
// Returns 1 if TRUE, 0 if FALSE.
// Standard libc character classification function.
int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}
