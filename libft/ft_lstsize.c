#include "libft.h"

// Counts the number of nodes in a linked list.
// Iterates through the list and increments a counter.
// Returns the total count of nodes.
int	ft_lstsize(t_list *lst)
{
	int	count;

	count = 0;
	while (lst)
	{
		count++;
		lst = lst->next;
	}
	return (count);
}
