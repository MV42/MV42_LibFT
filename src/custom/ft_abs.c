#include "libft.h"

// Computes the absolute value of an integer.
// Returns the positive equivalent of 'x'.
// Changes negative numbers to positive, leaves positives unchanged.
int	ft_abs(int x)
{
	if (x < 0)
		x *= -1;
	return (x);
}
