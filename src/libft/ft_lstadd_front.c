#include "libft.h"

// Adds a node to the front of a linked list.
// Sets 'new' as the first node and updates the list pointer.
// Does nothing if 'new' is NULL.
void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (!new)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	new->next = *lst;
	(*lst)->prev = new;
	new->index = (*lst)->index - 1;
	*lst = new;
}
