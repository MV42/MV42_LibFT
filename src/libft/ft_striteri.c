#include "libft.h"

// Applies a function to each character of a string with its index.
// Modifies the string 's' in place using function 'f'.
// Does nothing if 's' or 'f' is NULL.
void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;

	if (!s || !f)
		return ;
	i = 0;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}
